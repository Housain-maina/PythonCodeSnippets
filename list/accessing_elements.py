numbers = [5, 6, 7, 8, 9]  # list of integers

# Accessing elements in a list
print(numbers[0])  # accessing the first element in the list

print(numbers[-1])  # accessing the last element in the list

print(numbers[2:])  # accessing the elements from index 2 to the end of the list

print(numbers[:3])  # accessing the elements from the beginning to index 3

print(numbers[1:4])  # accessing the elements from index 1 to index 4

print(numbers.index(7))  # finding the index of an element in the list
